package com.qinzz.edu.entity;

public class Teacher {
	
		public String teacherName;
		public String teacherId;
		public String teacherNation;
		public int teacherAge;
		public int teacherIDcard;
		public String getTeacherName() {
			return teacherName;
		}
		public void setTeacherName(String teacherName) {
			this.teacherName = teacherName;
		}
		public String getTeacherId() {
			return teacherId;
		}
		public void setTeacherId(String teacherId) {
			this.teacherId = teacherId;
		}
		public String getTeacherNation() {
			return teacherNation;
		}
		public void setTeacherNation(String teacherNation) {
			this.teacherNation = teacherNation;
		}
		public int getTeacherAge() {
			return teacherAge;
		}
		public void setTeacherAge(int teacherAge) {
			this.teacherAge = teacherAge;
		}
		public int getTeacherIDcard() {
			return teacherIDcard;
		}
		public void setTeacherIDcard(int teacherIDcard) {
			this.teacherIDcard = teacherIDcard;
		}
		
		
	}

